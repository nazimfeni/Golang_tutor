package main

import "fmt"

func main() {
	// Variables
	num1 := 20
	num2 := 8.2

	// Addition
	sum := float64(num1) + num2

	// Subtraction
	diff := float64(num1) - num2

	fmt.Println("Number 1:", num1)
	fmt.Println("Number 2:", num2)
	fmt.Println("Addition:", sum)
	fmt.Println("Subtraction:", diff)
}

